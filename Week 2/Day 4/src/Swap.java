    // Swap the values of the variables

public class Swap {
    public static void main(String[] args) {
        int a = 123;
        int b = 526;
        System.out.println(a);
        System.out.println(b);
        int ra = b;
        int rb = a;
        System.out.println(ra);
        System.out.println(rb);
    }
}
